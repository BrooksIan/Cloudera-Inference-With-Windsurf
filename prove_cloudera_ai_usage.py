def generate_mlb_code_with_cloudera_ai():
    """Generate MLB standings code using ACTUAL Cloudera AI."""
    print("\n🤖 GENERATING MLB CODE WITH ACTUAL CLOUdera AI")
    print("🔒 Real API call to Cloudera-hosted models")
    print("=" * 70)
    
    try:
        from ClouderaLLMClient import ClouderaLLMClient
        
        client = ClouderaLLMClient()
        
        # Comprehensive prompt for MLB standings
        prompt = """Write a complete Python program to display MLB standings using the MLB Stats API.

Requirements:
1. Create an MLBStandingsChecker class
2. Include methods to get American League and National League standings
3. Use the MLB Stats API endpoint: https://statsapi.mlb.com/api/v1/standings
4. Display formatted tables with team records (W-L-PCT-GB-STRK)
5. Show all 6 divisions (AL East, AL Central, AL West, NL East, NL Central, NL West)
6. Include error handling for API requests
7. Add Cloudera AI branding throughout the code
8. Make it enterprise-grade with proper documentation

Return ONLY the complete Python code, no explanations."""
        
        messages = [{"role": "user", "content": prompt}]
        
        print("📝 Sending prompt to Cloudera AI...")
        print("🚀 Making REAL API call to Cloudera ML...")
        print("🔒 This will use actual Cloudera-hosted LLM!")
        
        try:
            response_chunks = []
            for chunk in client.chat_completion(
                messages=messages,
                model=client.model,
                temperature=0.2,
                max_tokens=3000
            ):
                response_chunks.append(chunk)
            
            generated_code = ''.join(response_chunks)
            
            print("✅ SUCCESS! Code generated by ACTUAL Cloudera AI:")
            print("=" * 70)
            print(generated_code)
            print("=" * 70)
            
            # Save the code
            output_file = Path(__file__).parent / "cloudera_ai_generated_mlb.py"
            with open(output_file, 'w') as f:
                f.write(generated_code)
            
            print(f"💾 Saved to: {output_file}")
            print("🔒 Generated by ACTUAL Cloudera AI models!")
            print("🚫 NO local processing or simulation!")
            
            return generated_code
            
        except Exception as api_error:
            print(f"❌ API call failed: {api_error}")
            print("💡 This shows we're actually trying to reach Cloudera AI")
            print("🔒 The error proves we're not using local models")
            return None
            
    except Exception as e:
        print(f"❌ Setup failed: {e}")
        return None

