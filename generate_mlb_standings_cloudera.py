    def get_standings(self, league_id: int, season: Optional[int] = None) -> Dict:
        """
        Get MLB standings for specified league and season.
        
        Args:
            league_id: League ID (103 for AL, 104 for NL)
            season: Season year (defaults to current year)
            
        Returns:
            Dictionary containing standings data
        """
        try:
            target_season = season or self.current_year
            
            # Get standings data
            url = f"{self.base_url}/standings"
            params = {
                'leagueId': league_id,
                'season': target_season,
                'standingsTypes': 'regularSeason'
            }
            
            response = requests.get(url, params=params, timeout=10)
            response.raise_for_status()
            
            data = response.json()
            return data.get('records', [])
            
        except requests.RequestException as e:
            print(f"❌ Error fetching MLB standings: {e}")
            return []
        except Exception as e:
            print(f"❌ Unexpected error: {e}")
            return []
    
