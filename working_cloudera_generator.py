def generate_with_working_cloudera_auth():
    """Generate code using the working Cloudera authentication."""
    print("🤖 Working Cloudera AI Generator")
    print("🔒 Using successful authentication method")
    print("=" * 60)
    
    # Get configuration
    base_url = os.getenv("WINDSURF_LLM_BASE_URL")
    api_key = os.getenv("WINDSURF_LLM_API_KEY")
    model = os.getenv("WINDSURF_LLM_MODEL")
    
    print(f"✅ Base URL: {base_url[:50]}...")
    print(f"✅ Model: {model}")
    print(f"✅ API Key: {api_key[:20]}...")
    
    # Validate Cloudera endpoint
    if not base_url or "cloudera.site" not in base_url:
        print("❌ Not a Cloudera endpoint!")
        return None
    
    # Use the working completions endpoint (not chat/completions)
    completions_url = f"{base_url.rstrip('/')}/completions"
    
    # Prepare prompt for 25 primes
    prompt = """Write a complete Python program to find the first 25 prime numbers.

Requirements:
1. Create an efficient is_prime(n) function
2. Create a find_first_n_primes(n) function  
3. Create a main() function that displays results
4. Show all 25 prime numbers numbered 1-25
5. Display the 25th prime number specifically
6. Include a summary with total count and sum
7. Add clear comments and good formatting
8. Make it efficient and well-structured

Return ONLY the Python code, no explanations."""
    
    # Prepare payload
    payload = {
        "model": model,
        "prompt": prompt,
        "max_tokens": 2000,
        "temperature": 0.2
    }
    
    # Use the successful Bearer token authentication
    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json",
        "X-Requested-By": "cascade-agent",
        "X-XSRF-Header": "true"
    }
    
    print(f"\n📝 Making API call to: {completions_url}")
    print("🚀 Using working Bearer token authentication...")
    
    try:
        response = requests.post(
            completions_url,
            headers=headers,
            json=payload,
            timeout=30
        )
        
        print(f"📊 Status Code: {response.status_code}")
        
        if response.status_code == 200:
            result = response.json()
            generated_code = result["choices"][0]["text"].strip()
            
            print("✅ SUCCESS! Code generated by Cloudera AI:")
            print("=" * 60)
            print(generated_code)
            print("=" * 60)
            
            # Save the code
            output_file = Path(__file__).parent / "cloudera_25_primes_working.py"
            with open(output_file, 'w') as f:
                f.write(generated_code)
            
            print(f"💾 Saved to: {output_file}")
            print("🔒 Generated using Cloudera AI ONLY!")
            print("🚫 NO Windsurf models used!")
            
            return generated_code
            
        else:
            print(f"❌ API call failed: {response.status_code}")
            print(f"Response: {response.text[:200]}...")
            return None
            
    except Exception as e:
        print(f"❌ Request failed: {e}")
        return None

